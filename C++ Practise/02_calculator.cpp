#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a, b, c;
    cout << "Enter two numbers\n";
    cin >> a >> b;
    c = a + b;
    cout << "Sum is: " << c << endl;
    c = a - b;
    cout << "Sub is: " << c << endl;
    c = a * b;
    cout << "Mul is: " << c << endl;
    c = a / b;
    cout << "Div is: " << c << endl;
    c = a % b;
    cout << "Rem is: " << c << endl;

    return 0;
}