/*
C++ language was designed with the main intention of adding object-oriented features to C language(C with classes)
As the size of program increases, readability, maintainability and bug-free nature of programs decreases
This was the major problem with languages like C which relied upon functions or procedure(hence the name procedural programming language)
-->As a result, the possibility of not addressing the problem in an effecitve manner was high

-->Also, as the data was almost neglected, data security was easily compromised

-->Using classes solves this problem by modelling progarm as areal world scenario
*/

/*
Procedure oriented programming(POP):
-->Consist of writing a set of instructions for the computer to follow

-->Main focus is on functions and not on flow of data

-->Function can either use local or global data

-->Data moves from function to function
*/

/*
Object oriented programming(OOP):
-->Works on the concept of classes and objects

-->A class is a template to create objects

-->Treats data as a critical element

-->Decomposes the problem in objects and builds data and functions around the objects
*/

/*
Basic concepts in OOP:
Classes: Basic template for creating objects

Objects: Basic run time entities

Data Abtraction & Encapsulation: Wrapping data and functions single unit

Inheritance: Properties of one class can be inherited into others

Polymorphism: Ability to take more than one forms

Dynamic Binding: Code which will execute is not known untill the program runs

Message Passing: Object.message(Information) call format
*/

/*
Benefits of OOPs:
-->Better code reusibility using objects and Inheritance

-->Principal of data hiding helps build secure systems

-->Multiple objects can co-exist without any inheritance

-->Software complexity can be easily managed
*/
