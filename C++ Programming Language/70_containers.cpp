/*
Containers
1.Sequence container: Linear fashion
eg: vector, list, queue

Vector:
-->Random access: fast
-->Insertion/Deletion: slow
-->Insertion at the end: fast

List:
-->Random access: slow
-->Insertion/Deletion: fast
-->Insertion at the end: fast

2.Associative container: Direct access
eg: tree, set, multiset, map, multimap

-->All operations: fast
-->Except random access: slow

3.Derived container: Real world modelling
eg: stack, queue, priority-queue

-->Depends-->Data structure
*/